def subtract_numbers(num1, num2):
    """
    Subtracts the second number from the first number.

    Args:
        num1 (int or float): The first number.
        num2 (int or float): The second number.

    Returns:
        int or float: The result of num1 - num2.
    """
    return num1 - num2